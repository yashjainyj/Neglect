package com.example.neglect.GetterSetter;

public class GetIndex {
    int i, c;

    public GetIndex(int i, int c) {
        this.i = i;
        this.c = c;
    }

    public int getI() {
        return i;
    }

    public void setI(int i) {
        this.i = i;
    }

    public int getC() {
        return c;
    }

    public void setC(int c) {
        this.c = c;
    }
}
